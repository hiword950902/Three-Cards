package com.neuedu.Student;

public class Stu{
			private int no;
			private String name;
			private String sex;
			
			public Stu() {
				super();
			}

			public Stu(int no, String name, String sex) {
				super();
				this.no = no;
				this.name = name;
				this.sex = sex;
			}

			public int getNo() {
				return no;
			}

			public void setNo(int no) {
				this.no = no;
			}

			public String getName() {
				return name;
			}

			public void setName(String name) {
				this.name = name;
			}

			public String getSex() {
				return sex;
			}

			public void setSex(String sex) {
				this.sex = sex;
			}
			
			
	
}
